// logical operators
// && || !
console.log(5 > 7 && 8 > 3); // false
console.log(5 == 0 || 5 > 1); // false or true --> true
const wrong = false;
console.log(!wrong); // true

// 1. Boolean &&
// - false && true -> true
// - false && false -> false
// - true && false -> false
// - true && true -> true

// 2. Boolean ||
// false || true -> true
// true || false -> true
// false || true -> true
// true || true -> true
// false || false -> false

